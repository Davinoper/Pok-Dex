class Pokemon{
  int id;
  String nome;
  double attack;
  double speed;
  double life;
  double defense;
  String element;
  String imageUrl;

  Pokemon(int id, String nome, double attack, double speed, double life, double defense, String element, String imageUrl):
    this.id =id,
    this.nome = nome,
    this.attack = attack,
    this.speed = speed,
    this.life = life,
    this.defense = defense,
    this.element = element,
    this.imageUrl = imageUrl;
}



